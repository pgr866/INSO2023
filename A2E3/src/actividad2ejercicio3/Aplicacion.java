package actividad2ejercicio3;

public class Aplicacion {

	private int numUsuarios;
	private int numProductos;
	private int numOfertas;
	private int numPeriodos;

	public int getNumUsuarios() {
		return this.numUsuarios;
	}

	public void setNumUsuarios(int numUsuarios) {
		this.numUsuarios = numUsuarios;
	}

	public int getNumProductos() {
		return this.numProductos;
	}

	public void setNumProductos(int numProductos) {
		this.numProductos = numProductos;
	}

	public int getNumOfertas() {
		return this.numOfertas;
	}

	public void setNumOfertas(int numOfertas) {
		this.numOfertas = numOfertas;
	}

	public int getNumPeriodos() {
		return this.numPeriodos;
	}

	public void setNumPeriodos(int numPeriodos) {
		this.numPeriodos = numPeriodos;
	}
}
